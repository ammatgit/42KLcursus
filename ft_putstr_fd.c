#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	ix;

	ix = 0;
	if (!s)
		return ;
	while (s[ix])
	{
		ft_putchar_fd(s[ix], fd);
		ix++;
	}
}
